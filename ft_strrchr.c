char	*ft_strrchr(const char *s, int c)
{
	int		len;
	char	*s_;

	s_ = (char *) s;
	len = 0;
	while (*s_ != 0)
	{
		s_++;
		len++;
	}
	if (c == 0)
		return (s_);
	while (len >= 0)
	{
		if (*s_ == c)
			return (s_);
		s_--;
		len--;
	}
	return (0);
}
